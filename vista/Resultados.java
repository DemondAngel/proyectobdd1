    public void Charge(List<Venta> ventas, ProductoVendido productoVendido, List<OrdenTerritorio> ordenes, int opcion) throws SQLException{
        this.ventas = ventas;
        this.productoVendido = productoVendido;
        this.ordenes = ordenes;
        this.opcion = opcion;
        DefaultTableModel Modelo=(DefaultTableModel) jTable1.getModel();
        int len=opcion == 0 ? this.ventas.size() : (opcion==1 ? 1 : this.ordenes.size());
        String mString[][] = new String[len][3];
        for(int i=0; i<len;i++)
        {
            if(opcion==0)
            {
                mString[i][0] = String.valueOf(this.ventas.get(i).getTerritoryID());
                mString[i][1] = String.valueOf(this.ventas.get(i).getNameTerritory());
                mString[i][2] = String.valueOf(this.ventas.get(i).getTotalVentas());
            
            }
            else if(opcion==1)
            {
                mString[i][0] = String.valueOf(this.productoVendido.getProductID());
                mString[i][1] = String.valueOf(this.productoVendido.getNombre());
                mString[i][2] = String.valueOf(this.productoVendido.getTotalVentas());
            
            }
            else if(opcion==2)
            {
                mString[i][0] = String.valueOf(this.ordenes.get(i).getIdTerritorioCliente());
                mString[i][1] = String.valueOf(this.ordenes.get(i).getIdTerritorioOrden());
                mString[i][2] = String.valueOf(this.ordenes.get(i).getNameTerritorioOrden());
            
            }

        }

        jTable1.setModel(new javax.swing.table.DefaultTableModel(mString, opcion == 0 || opcion == 1? new String[]{"ID", "Nombre", "Ventas"} : new String[]{"ID Cliente", "ID Terrirorio", "Nombre del Territorio"}));


    }
